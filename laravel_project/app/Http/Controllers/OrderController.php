     */
    public function index()
    {
        return view('admin.order.index', ['orders' => Order::all()]);
    }

    public function order_detail(Order $order)
    {
        return view('admin.order_info.index', ['order_infos' => OrderInfo::where('order_id', $order->id)->get(), "order" => $order]);
    }

